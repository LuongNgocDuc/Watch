package com.example.luongduc.watch.modul;

public class Watch {
    public int imgWatch;
    public String nameWatch;

    public Watch(int imgWatch, String nameWatch) {
        this.imgWatch = imgWatch;
        this.nameWatch = nameWatch;
    }

    public int getImgWatch() {
        return imgWatch;
    }

    public void setImgWatch(int imgWatch) {
        this.imgWatch = imgWatch;
    }

    public String getNameWatch() {
        return nameWatch;
    }

    public void setNameWatch(String nameWatch) {
        this.nameWatch = nameWatch;
    }
}
