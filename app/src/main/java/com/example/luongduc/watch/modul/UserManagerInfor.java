package com.example.luongduc.watch.modul;

public class UserManagerInfor {
    public int imgManager;
    public String nameManager;

    public UserManagerInfor(int imgManager, String nameManager) {
        this.imgManager = imgManager;
        this.nameManager = nameManager;
    }

    public int getImgManager() {
        return imgManager;
    }

    public void setImgManager(int imgManager) {
        this.imgManager = imgManager;
    }

    public String getNameManager() {
        return nameManager;
    }

    public void setNameManager(String nameManager) {
        this.nameManager = nameManager;
    }
}
